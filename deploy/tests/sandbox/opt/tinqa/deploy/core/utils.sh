#!/usr/bin/env bash

###############################################################################
# TinQa Deployment Framework
# Utility Functions
###############################################################################

set -Eeuo pipefail

###############################################################################
# Retry
###############################################################################

retry() {

    local retries="${1}"
    shift

    local attempt=1

    until "$@"; do

        if (( attempt >= retries )); then
            return 1
        fi

        log_warning "Attempt ${attempt}/${retries} failed. Retrying in ${RETRY_DELAY}s..."

        sleep "${RETRY_DELAY}"

        ((attempt++))

    done

}

###############################################################################
# Execute Remote Command
###############################################################################

remote_exec() {

    local command="$1"

    ssh \
        "${SSH_OPTIONS[@]}" \
        "${SSH_TARGET}" \
        "${command}"

}

###############################################################################
# Execute Remote Command (Strict)
###############################################################################

remote_exec_safe() {

    local command="$1"

    remote_exec "set -Eeuo pipefail; ${command}"

}

###############################################################################
# Copy Project
###############################################################################

rsync_project() {

    local excludes=()

    for item in "${RSYNC_EXCLUDES[@]}"; do
        excludes+=(--exclude "$item")
    done

    rsync \
        "${RSYNC_OPTIONS[@]}" \
        "${excludes[@]}" \
        "${LOCAL_PROJECT_DIR}/" \
        "${SSH_TARGET}:${REMOTE_PROJECT_DIR}/"

}

###############################################################################
# Check SSH Connectivity
###############################################################################

check_ssh() {

    remote_exec "echo Connected" >/dev/null

}

###############################################################################
# Execute With Timer
###############################################################################

time_execution() {

    local start
    local finish

    start=$(date +%s)

    "$@"

    finish=$(date +%s)

    echo $((finish-start))

}

###############################################################################
# Verify Remote Command Exists
###############################################################################

remote_command_exists() {

    local cmd="$1"

    remote_exec "command -v ${cmd} >/dev/null"

}

###############################################################################
# Verify Remote File Exists
###############################################################################

remote_file_exists() {

    local file="$1"

    remote_exec "[ -f '${file}' ]"

}

###############################################################################
# Verify Remote Directory Exists
###############################################################################

remote_directory_exists() {

    local dir="$1"

    remote_exec "[ -d '${dir}' ]"

}

###############################################################################
# Get Remote Python Version
###############################################################################

remote_python_version() {

    remote_exec "${PYTHON_EXECUTABLE} --version"

}

###############################################################################
# Get Remote OS
###############################################################################

remote_os() {

    remote_exec "grep PRETTY_NAME /etc/os-release | cut -d= -f2 | tr -d '\"'"

}

###############################################################################
# Get Remote Hostname
###############################################################################

remote_hostname() {

    remote_exec "hostname"

}

###############################################################################
# Get Remote IP
###############################################################################

remote_ip() {

    remote_exec "hostname -I | awk '{print \$1}'"

}

###############################################################################
# Get Remote Uptime
###############################################################################

remote_uptime() {

    remote_exec "uptime -p"

}

###############################################################################
# Service Status
###############################################################################

service_status() {

    local service="$1"

    remote_exec "systemctl is-active ${service}"

}

###############################################################################
# Package Installed
###############################################################################

package_installed() {

    local package="$1"

    remote_exec "dpkg -s ${package} >/dev/null 2>&1"

}

###############################################################################
# Python Package Installed
###############################################################################

python_package_installed() {

    local package="$1"

    remote_exec "${REMOTE_VENV}/bin/python -c 'import ${package}' >/dev/null 2>&1"

}

###############################################################################
# Separator
###############################################################################

separator() {

    printf '%*s\n' 80 '' | tr ' ' '-'

}

###############################################################################
# Human Time
###############################################################################

human_time() {

    local total="$1"

    local hours=$((total/3600))
    local minutes=$(((total%3600)/60))
    local seconds=$((total%60))

    if ((hours>0)); then
        printf "%02dh %02dm %02ds" "$hours" "$minutes" "$seconds"
    elif ((minutes>0)); then
        printf "%02dm %02ds" "$minutes" "$seconds"
    else
        printf "%02ds" "$seconds"
    fi

}

###############################################################################
# Timestamp
###############################################################################

timestamp() {

    date "+%Y-%m-%d %H:%M:%S"

}

