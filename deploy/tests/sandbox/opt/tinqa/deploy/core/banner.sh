print_banner() {

    clear 2>/dev/null || true

    log_line

    printf "%b\n" "$(color_cyan "████████╗██╗███╗   ██╗ ██████╗  █████╗ ")"
    printf "%b\n" "$(color_cyan "╚══██╔══╝██║████╗  ██║██╔═══██╗██╔══██╗")"
    printf "%b\n" "$(color_cyan "   ██║   ██║██╔██╗ ██║██║   ██║███████║")"
    printf "%b\n" "$(color_cyan "   ██║   ██║██║╚██╗██║██║▄▄ ██║██╔══██║")"
    printf "%b\n" "$(color_cyan "   ██║   ██║██║ ╚████║╚██████╔╝██║  ██║")"
    printf "%b\n" "$(color_cyan "   ╚═╝   ╚═╝╚═╝  ╚═══╝ ╚══▀▀═╝ ╚═╝  ╚═╝")"

    printf "\n"

    printf "%b\n" "$(bold "$FRAMEWORK_NAME")"

    printf "\n"

    log_line

    printf "%-24s : %s\n" "Framework Version" "$FRAMEWORK_VERSION"
    printf "%-24s : %s\n" "Project" "$PROJECT_NAME"
    printf "%-24s : %s\n" "Target Device" "$SSH_TARGET"
    printf "%-24s : %s\n" "Remote Directory" "$REMOTE_PROJECT_DIR"
    printf "%-24s : %s\n" "Service" "$SERVICE_NAME"
    printf "%-24s : %s\n" "Python" "$PYTHON_EXECUTABLE"
    printf "%-24s : %s\n" "Started At" "$(timestamp)"
    printf "%-24s : %s\n" "Log File" "$LOG_FILE"

    log_line

    printf "\n"

}

