#!/usr/bin/env bash
###############################################################################
#
# Target Execution Wrapper
#
###############################################################################

set -Eeuo pipefail

###############################################################################
# Execute on current target
###############################################################################

target_exec() {

    if [[ "${DEPLOY_MODE}" == "TEST" ]]
    then
        "$@"
    else
        remote_exec "$@"
    fi

}

export -f target_exec