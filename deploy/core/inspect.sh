#!/usr/bin/env bash
###############################################################################
#
# System Inspection Engine
#
###############################################################################

set -Eeuo pipefail

###############################################################################
# Storage
###############################################################################

INSPECT_KEYS=()
INSPECT_VALUES=()

###############################################################################
# Set
###############################################################################

inspect_set() {

    local key="$1"
    local value="$2"

    local i

    for ((i=0; i<${#INSPECT_KEYS[@]}; i++))
    do
        if [[ "${INSPECT_KEYS[$i]}" == "${key}" ]]
        then
            INSPECT_VALUES[$i]="${value}"
            return
        fi
    done

    INSPECT_KEYS+=("${key}")
    INSPECT_VALUES+=("${value}")
}

###############################################################################
# Get
###############################################################################

inspect_get() {

    local key="$1"
    local i

    for ((i=0; i<${#INSPECT_KEYS[@]}; i++))
    do
        if [[ "${INSPECT_KEYS[$i]}" == "${key}" ]]
        then
            echo "${INSPECT_VALUES[$i]}"
            return
        fi
    done
}

###############################################################################
# Clear
###############################################################################

inspect_clear() {

    INSPECT_KEYS=()
    INSPECT_VALUES=()
}
###############################################################################
# Helpers
###############################################################################

inspect_yes() {

    [[ "$(inspect_get "$1")" == "yes" ]]

}

inspect_no() {

    [[ "$(inspect_get "$1")" == "no" ]]

}

inspect_dump() {

    echo
    echo "=============================================================="
    echo "Inspection Result"
    echo "=============================================================="

    local key

    for key in "${!INSPECT[@]}"
    do
        printf "%-35s : %s\n" "$key" "${INSPECT[$key]}"
    done | sort

    echo
}

###############################################################################
# Export
###############################################################################

export -f inspect_set
export -f inspect_get
export -f inspect_clear
export -f inspect_yes
export -f inspect_no
export -f inspect_dump