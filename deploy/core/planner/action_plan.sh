#!/usr/bin/env bash
###############################################################################
#
# Deployment Action Planner
#
###############################################################################

set -Eeuo pipefail

###############################################################################
# Storage
###############################################################################

ACTIONS=()
ACTION_REASONS=()

###############################################################################
# Reset Planner
###############################################################################

plan_clear() {

    ACTIONS=()
    ACTION_REASONS=()

}

###############################################################################
# Add Action
#
# Usage:
#   plan_add run_02_cleanup
#
#   plan_add run_02_cleanup "Cleanup previous deployment"
#
###############################################################################

plan_add() {

    local action="$1"
    local reason="${2:-}"

    ACTIONS+=("${action}")
    ACTION_REASONS+=("${reason}")

}

###############################################################################
# Check Action Exists
###############################################################################

plan_has() {

    local action

    for action in "${ACTIONS[@]}"
    do
        [[ "${action}" == "$1" ]] && return 0
    done

    return 1

}

###############################################################################
# Get Reason
###############################################################################

plan_reason() {

    local index="$1"

    echo "${ACTION_REASONS[$index]:-}"

}

###############################################################################
# Print Plan
###############################################################################

plan_print() {

    echo
    echo "=============================================================="
    echo "Deployment Plan"
    echo "=============================================================="

    local i
    local reason

    for ((i=0; i<${#ACTIONS[@]}; i++))
    do
        reason="${ACTION_REASONS[$i]:-}"

        if [[ -n "${reason}" ]]
        then
            printf "%2d. %-30s : %s\n" \
                "$((i+1))" \
                "${ACTIONS[$i]}" \
                "${reason}"
        else
            printf "%2d. %s\n" \
                "$((i+1))" \
                "${ACTIONS[$i]}"
        fi
    done

    echo

}

###############################################################################
# Export
###############################################################################

export -f plan_clear
export -f plan_add
export -f plan_has
export -f plan_reason
export -f plan_print